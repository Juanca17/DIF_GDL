              <p class="card-text">Con un enfoque psico educativo, a través de exposiciones, reflexiones individuales y dinámicas grupales, se plantea un proceso de aprendizaje para que las participantes desarrollen nuevas habilidades y formas de comportamiento, que permitan establecer relaciones de pareja en un plano de igualdad.</p>
              <p class="card-text">Los contenidos están centrados en la equidad de género y el empoderamiento de las mujeres. En 14 sesiones, una vez por semana con duración de dos horas, se revisan temas de: empoderamiento, autoestima, redes de apoyo, comunicación familiar, proyecto de vida, autocuidado, crianza respetuosa, cuerpo y sexualidad, derechos humanos, resiliencia ante situaciones de violencia, manejo y resolución de conflictos, señales de peligro, construcción de redes familiares y sociales, entre otros.</p>
